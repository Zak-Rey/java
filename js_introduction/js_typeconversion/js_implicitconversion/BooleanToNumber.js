// if boolean is used, true is 1, false is 0

let result;

result = '4' - true;
console.log(result); // 3

result = 4 + true;
console.log(result); // 5

result = 4 + false;
console.log(result); // 4

//Java script considers 0 as false and all non-zeros as true. It true is converted to a number it always result to 1